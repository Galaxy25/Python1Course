# Basic float
my_float = 1.2

print(my_float, type(my_float))

# Int to float
my_int = 1
my_float = float(my_int)

print(my_float, type(my_float))

# Float to int
my_float = 1.2
my_int = int(my_float)

print(my_int, type(my_int))

# String to float
my_string = "1.2"
my_float = float(my_string)

print(my_float, type(my_float))

# Float from non-floor division
my_float = 7 / 6

print(my_float, type(my_float))

# Float to string
my_float = 1.2
my_string = str(my_float)

print(my_string, type(my_string))
