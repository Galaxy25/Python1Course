# Declare a string variable
my_string = "Hello World"

# Declare another string variable
my_string2 = " My name is Jake"

# Add them together
my_string += my_string2

# Print the result
print(my_string)

# Use the + operator in a plain print statement
print("Hello World" + " My name is Jake")

# Use the * operator to print the string 3 times
print("Hello World " * 3)