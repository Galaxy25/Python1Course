# Declaring some variables
x = 5
y = 2

# Print the sum of x and y
print("The sum of x and y is:", x + y)

# Make a new variable called z and set it to the sum of x and y
z = x + y

# Print z
print("z is:", z)

# Assign x to the value of x + y
x = x + y

# Print x
print("x is:", x)

# Reset the value of x and use shorthand assignment 
# to set it to the sum of x and y
x = 5

x += y

# Test the value of x again
print("x is:", x)